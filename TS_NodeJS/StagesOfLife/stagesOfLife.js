var age = 25;
if (age < 2) {
    console.log("This human is a baby.");
}
else if (age >= 2 && age < 4) {
    console.log("This human is a toddler.");
}
else if (age >= 4 && age < 13) {
    console.log("This human is a kid.");
}
else if (age >= 13 && age < 20) {
    console.log("This human is a teenager.");
}
else if (age >= 20 && age < 65) {
    console.log("This human is an adult.");
}
else {
    console.log("This human is an elder.");
}
